package com.example.gestura;

public class ButtonModelClass {
    private int imgButton1;
    private int imgButton2;

    public ButtonModelClass(int imgButton1, int imgButton2) {
        this.imgButton1 = imgButton1;
        this.imgButton2 = imgButton2;
    }

    public int getImgButton1() {
        return imgButton1;
    }

    public int getImgButton2() {
        return imgButton2;
    }
}
